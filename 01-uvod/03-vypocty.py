a = int(input("zadaj cislo a: ")) # input je prikazom vstupu, vypise na obrazovku text v zatvorke a to co uzivatel napise na klavesnici, ulozi do premennej a 
b = int(input("zadaj cislo b: ")) # input vracia vzdy vysledok vo formate retazca (postupnost znakov resp. text), funkcia int() pretypuje retazec na cele cislo 
sucet = a + b
print("sucet je: ", sucet)
sucin = a * b
print("sucin je: ", sucin)
rozdiel = a - b
print("rozdiel je: ", rozdiel)
podiel = a / b
print("podiel je: ", podiel)